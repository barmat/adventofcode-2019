def amp_circuit(opcodes, phase_settings):
	input_signal = 0
	for i in range(0, len(phase_settings)):
		input_signal = Intcode(opcodes).run(int(phase_settings[i]), input_signal)[0][0]
	return input_signal

def find_highest_signal(opcodes):
	all_phase_settings = [''.join(p) for p in permutations('01234')]
	highest_signal = 0
	for phase_setting in all_phase_settings:
		signal = amp_circuit(opcodes, phase_setting)
		if signal > highest_signal:
			highest_signal = signal
	return highest_signal


